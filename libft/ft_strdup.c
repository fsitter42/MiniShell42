#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	int		length;
	char	*dest;

	i = 0;
	length = ft_strlen(s);
	dest = (char *)malloc(sizeof(char) * (length + 1));
	if (dest == NULL)
		return (NULL);
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
